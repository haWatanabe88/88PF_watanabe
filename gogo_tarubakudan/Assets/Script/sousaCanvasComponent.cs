using UnityEngine;

public class sousaCanvasComponent : MonoBehaviour
{
    public static sousaCanvasComponent Instance { get; private set; }

    private void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }

        Instance = this;
        DontDestroyOnLoad(gameObject);
    }
}
